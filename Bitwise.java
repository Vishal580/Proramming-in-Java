import java.util.*;
class Bitwise
{
  public static void main(String args[])
  {
     int A=60,B=13;
     System.out.println(A&B);
     System.out.println(A|B);
     System.out.println(A^B);
     System.out.println(~A);
     System.out.println(A<<2);
     System.out.println(A>>2);
     System.out.println(A>>>2);
     
  }
}